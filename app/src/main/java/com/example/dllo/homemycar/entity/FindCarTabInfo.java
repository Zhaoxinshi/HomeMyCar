import android.support.v4.app.Fragment;

import com.example.dllo.homemycar.findcarfragment.NewCarFragment;
import com.example.dllo.homemycar.findcarfragment.SecondHandFragment;

import java.util.ArrayList;
import java.util.List;

/**
 * Created by dllo on 16/9/23.
 */
public class FindCarTabInfo {
    private Fragment fragment;
    private String title;

    public FindCarTabInfo(Fragment fragment, String title) {
        this.fragment = fragment;
        this.title = title;
    }

    public Fragment getFragment() {
        return fragment;
    }

    public void setFragment(Fragment fragment) {
        this.fragment = fragment;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public static List<FindCarTabInfo> getFindCarInfo() {
        List<FindCarTabInfo> tabInfos = new ArrayList<>();
        tabInfos.add(new FindCarTabInfo(new NewCarFragment(), "新车"));
        tabInfos.add(new FindCarTabInfo(new SecondHandFragment(), "二手车"));
        return tabInfos;

    }
}
